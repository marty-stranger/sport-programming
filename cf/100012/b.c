#include <stdio.h>
#include <math.h>

int main() {
	double x[3], y[3];

	freopen("input.txt", "r", stdin);
	freopen("output.txt", "w", stdout);


	int i;
	for (i = 0; i < 3; i++)
		scanf("%lf%lf", &x[i], &y[i]);

	double p[12][3] = {
		{0.000000000, 0.000000000, 0.000000000},
		{1.000000000, 0.000000000, 0.000000000},
		{1.000000000, 0.577350269, 1.511522628},
		{0.000000000, 0.577350269, 1.511522628},
		{0.500000000, 0.866025404, 0.000000000},
		{0.500000000, 1.222847494, 0.934172359},
		{0.500000000, -0.288675135, 1.511522628},
		{0.500000000, -0.645497224, 0.577350269},
		{-0.309016994, 0.755761314, 0.577350269},
		{1.309016994, 0.755761314, 0.577350269},
		{1.309016994, -0.178411045, 0.934172359},
		{-0.309016994, -0.178411045, 0.934172359}};
		
	double x1 = x[1] - x[0], y1 = y[1] - y[0];
	double x2 = x[2] - x[0], y2 = y[2] - y[0];
	
	if (x1*y2 - x2*y1 < 0) {
		double t = x1; x1 = x2; x2 = t;
		t = y1; y1 = y2; y2 = t;
	}
	
       	double phi = atan2(y1, x1);
	for (i = 0; i < 12; i++) {
        	double xx = p[i][0] * cos(phi) - p[i][1] * sin(phi);
        	double yy = p[i][0] * sin(phi) + p[i][1] * cos(phi);
        	p[i][0] = xx;
        	p[i][1] = yy;
	}
	
	for (i = 0; i < 12; i++) {
		p[i][0] += x[0], p[i][1] += y[0];
		printf("%lf %lf %lf\n", p[i][0], p[i][1], p[i][2]);
	}
		
	return 0;
}
